			messages: convo.messages.map((msg) => msg.id),
			versions: Object.fromEntries(
				Object.entries(convo.versions).map(([key, value]) => [
					key,
					value.map((messages) => {
						if (!messages) {
							return null;
						}
						return messages.map((msg) => {
							return msg.id;
						});
					}),
				])
			),
		});

		transaction.onerror = () => {
			console.error('Conversation save failed', transaction.error);
		};
	}, 100);

